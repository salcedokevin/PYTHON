"""
pedir 2 numero al usuario y hacer todas las operaciones basicas de una calculadora y mostrarlo por pantalla
"""

numero_1 = int(input("Escriba el primer numero: "))
numero_2 = int(input("Escriba el segundo numero: "))

print(f"La suma es {numero_1 + numero_2}")
print(f"La resta es {numero_1 - numero_2}")
print(f"La multiplicacion es {numero_1 * numero_2}")
print(f"La division es {numero_1 / numero_2}")
print(f"El residuo es {numero_1 % numero_2}")